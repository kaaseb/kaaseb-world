import { createClient } from '@/lib/supabase/server'
import { redirect } from 'next/navigation'
import { hasPermission } from '@/lib/permissions'
import { getProfileOrFallback, getEffectivePermissions } from '@/lib/profile'
import { TannoorNewForm } from '@/components/tannoor/TannoorNewForm'

export const dynamic = 'force-dynamic'

export default async function NewTannoorPage() {
  const supabase = await createClient()
  const { data: { user } } = await supabase.auth.getUser()
  if (!user) redirect('/login')

  const profile = await getProfileOrFallback(supabase, user)
  const permissions = await getEffectivePermissions(supabase, profile)
  if (!hasPermission(profile, permissions, 'tannoor.projects.create')) redirect('/tannoor')

  return <TannoorNewForm />
}
